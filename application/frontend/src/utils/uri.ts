export function uriHome() {
  return "/";
}

export function uriInfo(id: string) {
  return `/info/${id}`;
}

export function uriMenu(id: string) {
  return `/menu/${id}`;
}

export function uriTables(id: string) {
  return `/tables/${id}`;
}

export function uriQueue(id: string){
  return `/queue/${id}`;
}